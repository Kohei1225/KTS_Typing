            }
            else if(nowmoji.Equals("ん") && (NN || nextmoji.Equals("う") || nextmoji.Equals("い") || Henkan[nextmoji][0].Substring(0,1).Equals("Ｙ"))) str += "ＮＮ";
            else {  
                str += Henkan[nowmoji][0];
                //Debug.Log(i + " (nowmoji)" + Henkan[nowmoji][0]);
            }
        }
            
        
        return str;
    }

    //今打つべきローマ字の文字列を作るメソッド(返す配列、何文字目か(ひらがな)、テキストの番号,一文字前の文字)
    string[] MakeTypingRoma(string[] str, int CharNum, int text, string backstr)
    {
        string hitomoji = dic[text,1].Substring(CharNum,1);//一文字目の文字
        string futamoji = "";//二文字分の文字(ひらがな)
        string nextChar = "";//二文字目の文字(ひらがな)
        int kanryakuLength = 0;//二文字分読んだ場合の楽に書け得るパターンの数
        int count  = 0;
        bool tu = false;//前の文字が「っ」ならtrue
        bool kigou = false;//次の文字が記号系ならtrue
        bool NN = false;//次の文字が母音、記号、ナ行、ん、ならtrue。ＮＮって打たないといけない判定。
        bool boin = false;//次の文字が母音ならtrue;
        int Counter = 0;//配列に順番に値を入れていくために使うカウンター

        //Debug.Log("backstr = " + backstr);

        if(CharNum > 0 && dic[text,1].Substring(CharNum - 1,1).Equals("っ"))tu = true;

        //最後の文字じゃなかったら二文字文読んでみる
        if(CharNum + 1 < dic[text,1].Length){
            futamoji = dic[text,1].Substring(CharNum,2);
            nextChar = dic[text,1].Substring(CharNum + 1,1);
            if(nextChar.Equals("あ") | nextChar.Equals("え") | nextChar.Equals("お"))boin = true;
            if(nextChar.Equals("、") | nextChar.Equals("。") | nextChar.Equals("ー") | nextChar.Equals("！") | nextChar.Equals("？"))kigou = true;
            if(nextChar.Equals("な") | nextChar.Equals("に") | nextChar.Equals("ぬ") | nextChar.Equals("ね") | nextChar.Equals("の")
             | nextChar.Equals("ん") | boin )NN = true;
        }
        
        //そもそも二文字分の書き方がない場合は一文字分だけ追加する。
        if( !Henkan.ContainsKey(futamoji) ){

            //今の文字が「っ」の場合で「ltu」で表さなくていい時は先に追加しておく
            if(hitomoji.Equals("っ") && !NN && !kigou){
                //今見てる文字が「っ」だったら
                //Debug.Log("次の頭文字で表せる");
                for(int i = 0; i < Henkan[nextChar].Length; i++){
                    //「う」と「い」は一文字以外で表せる(「っう」は多分「WWU」で書ける)ので免除する
                    if(!(Henkan[nextChar][i].Substring(0,1).Equals("Ｕ") || Henkan[nextChar][i].Substring(0,1).Equals("Ｉ")))
                        str[i] = Henkan[nextChar][i].Substring(0,1);
                    Counter++;
                    //Debug.Log("" + Henkan[]);
                }    
            }

            for(int i = 0; i < Henkan[hitomoji].Length; i++)
                //前の文字が「っ」の時
                if(tu && !backstr.Equals("Ｕ") ){
                    //「い：Ｉ」と「う：Ｕ」の時は除外する
                    if( Henkan[hitomoji][i].Substring(0,1).Equals(backstr) &&
                      !(Henkan[hitomoji][i].Substring(0,1).Equals("Ｕ") || Henkan[hitomoji][i].Substring(0,1).Equals("Ｉ"))){
                        str[Counter + i] = Henkan[hitomoji][i];          
                        //Debug.Log("リストの頭文字[" + i + "] : " + Henkan[hitomoji][i].Substring(0,1));                
                      }

                }
                else str[Counter + i] = Henkan[hitomoji][i];

            if(hitomoji.Equals("ん")){
                //今見てる文字が「ん」でＮＮって打たなきゃいけない時
                if( NN | CharNum + 1 == dic[text,1].Length 
                | nextChar.Equals("い") | nextChar.Equals("う"))str[0] = "ＮＮ";
                else if( CharNum + 1 != dic[text,1].Length && Henkan[nextChar][0].Substring(0,1).Equals("Ｙ"))str[0] = "ＮＮ";
            }
            
            skipChar = false;
            return str;
        }

        //ちゃんと2文字読める状態だったらfirstLengthを更新する。
        kanryakuLength = Henkan[futamoji].Length;

        //組み合わせて作れる文字の場合の文字を追加していく。(拗音とか)
        for(int i = 0; i < kanryakuLength; i++){
            if( tu && !backstr.Equals("Ｕ") ){
                //前のひらがなが「っ」で前に打ったローマ字が頭文字と合ってたら
                if(Henkan[futamoji][i].Substring(0,1).Equals(backstr))
                    str[i] = Henkan[futamoji][i];
            }
            //前の文字が「っ」以外なら普通に全部入れる。
            else str[i] = Henkan[futamoji][i];

            //ここに来たということは2文字分のローマ字を打っているのでtrueにする。
            skipChar = true;
        }
        //if(skipChar)Debug.Log("飛ぶはず");
        //一文字目の書き方を追加していく(拗音)
        for(int i = 0; i < Henkan[hitomoji].Length; i++){
            for(int j = 0; j < Henkan[nextChar].Length; j++){
                if(tu && !backstr.Equals("Ｕ")){
                    //前の文字が「っ」かつ前のローマ字がＵ以外かつ頭文字と同じ
                    if( Henkan[hitomoji][i].Substring(0,1).Equals(backstr) ){
                        str[kanryakuLength + count] = Henkan[hitomoji][i];
                    }
                }
                else str[kanryakuLength + count] = Henkan[hitomoji][i];
                count++;
            }
        }    

        skipChar = true;

        count = 0;

        //２文字目の書き方を追加していく(拗音)
        for(int i = 0; i < Henkan[hitomoji].Length; i++){
            for(int j = 0; j < Henkan[nextChar].Length; j++){
                if(!string.IsNullOrEmpty(str[kanryakuLength + count])){
                    //for(int k = 0)
                    str[kanryakuLength + count] += Henkan[nextChar][j];
                }

                count++;
            }
        }
        skipChar = true;
        
        return str;
    }


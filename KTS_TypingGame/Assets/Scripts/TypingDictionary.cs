                 | nextmoji.Equals("な") | nextmoji.Equals("に") | nextmoji.Equals("ぬ") | nextmoji.Equals("ね") | nextmoji.Equals("の") | nextmoji.Equals("ん") 
                 | boin )NN = true;
            }
            else{
                futamoji = "";
                nextmoji = "";
                last  = true;
                NN = true;
            }
            //日本語のテキストの文字をキーにHenkanの値の配列の0番目を持ってくる
            //二文字分を表せる場合
            //Debug.Log(nextmoji);
            if(Henkan.ContainsKey(futamoji) && !last){
                str += Henkan[futamoji][0];
                i++;
            }
            
            //「っ」の後に「い」と「う」が来た時の判定
            else if(Char + i > 0 && dic[Text,1].Substring(Char + i - 1,1).Equals("っ") && (nowmoji.Equals("う") || nowmoji.Equals("い")))str += Henkan[nowmoji][1];
            //最後の文字じゃないかつ今見てる文字が「っ」で頭文字を二回打って表現できる時
            else if( tu && !NN && !last && !kigou){
                if(Henkan[nextmoji][0].Equals("Ｉ") || Henkan[nextmoji][0].Equals("Ｕ"))str += Henkan[nextmoji][1].Substring(0,1);
                //Debug.Log(i + " (nextmoji)" + Henkan[nextmoji][0].Substring(0,1));

    // 引数として受け取った配列の要素番号を並び替えるメソッド
    void Shuffle()
    {
        for(int i = 0; i < dic.GetLength(0); i++){
            //（説明１）現在の要素を預けておく
            string temp0 = dic[i,0]; 
            string temp1 = dic[i,1];
            //（説明２）入れ替える先をランダムに選ぶ
            int randomIndex = Random.Range(0, dic.GetLength(0)); 
            //（説明３）現在の要素に上書き
            dic[i,0] = dic[randomIndex,0]; 
            dic[i,1] = dic[randomIndex,1];
            //（説明４）入れ替え元に預けておいた要素を与える
            dic[randomIndex,0] = temp0; 
            dic[randomIndex,1] = temp1;
        }
    }

    //まだ打ってないローマ字(デフォルト)を作るメソッド(テキスト番号、何文字目からか)
    string CreateDefaultRoma(int Text,int Char)
    {
        //Debug.Log("Char : " + Char);
        string str = "";
        string futamoji = "";
        string nextmoji = "";
        string nowmoji = "";
        bool tu = false;//一文字目が「っ」ならtrue
        bool kigou = false;//次の文字が記号系ならtrue
        bool NN = false;//次の文字が母音、記号、ナ行、ん、ならtrue。ＮＮって打たないといけない判定。
        bool boin = false;//次の文字が母音ならtrue;
        bool last = false;//ラストの文字かどうか
        
        for(int i = 0; Char + i < dic[Text,1].Length; i++){
            //毎回ループの最初に初期化
            tu = false;
            boin = false;
            kigou = false;
            NN = false;

            nowmoji = dic[Text,1].Substring(Char + i,1);            
            //次に文字が存在したら実行する
            if(Char + i + 1 < dic[Text,1].Length){
                futamoji = dic[Text,1].Substring(Char + i,2);
                nextmoji = dic[Text,1].Substring(Char + i + 1,1);
                
                if(nowmoji.Equals("っ"))tu = true;
                if( nextmoji.Equals("あ") | nextmoji.Equals("え") | nextmoji.Equals("お") )boin = true;
                if( nextmoji.Equals("１") | nextmoji.Equals("２") | nextmoji.Equals("３") | nextmoji.Equals("４") | nextmoji.Equals("５")
                 | nextmoji.Equals("６") | nextmoji.Equals("７") | nextmoji.Equals("８") | nextmoji.Equals("９") | nextmoji.Equals("０")
                 | nextmoji.Equals("Ａ") | nextmoji.Equals("Ｂ") | nextmoji.Equals("Ｃ") | nextmoji.Equals("Ｄ") | nextmoji.Equals("Ｅ")
